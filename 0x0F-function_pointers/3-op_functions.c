#include "3-calc.h"

/**
 * op_add - Function Add.
 * @a: num1.
 * @b: num2.
 *
 * Return: sum.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function Sub.
 * @a: num1.
 * @b: num2.
 *
 * Return: sub.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function Multiply.
 * @a: num1.
 * @b: num2.
 *
 * Return: multiplication.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function Divide.
 * @a: num1.
 * @b: num2.
 *
 * Return: division.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function Modulus.
 * @a: num1.
 * @b: num2.
 *
 * Return: Mod.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
